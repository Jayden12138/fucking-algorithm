import { solveNQueens } from '@/51.n-皇后'
import { describe, expect, it } from 'bun:test'

describe('first', () => {
	it('solveNQueens', () => {
		expect(solveNQueens(4)).toEqual([
			['.Q..', '...Q', 'Q...', '..Q.'],
			['..Q.', 'Q...', '...Q', '.Q..'],
		])
	})

	it('5 queens', () => {
		expect(solveNQueens(5)).toEqual([
			['Q....', '..Q..', '....Q', '.Q...', '...Q.'],
			['Q....', '...Q.', '.Q...', '....Q', '..Q..'],
			['.Q...', '...Q.', 'Q....', '..Q..', '....Q'],
			['.Q...', '....Q', '..Q..', 'Q....', '...Q.'],
			['..Q..', 'Q....', '...Q.', '.Q...', '....Q'],
			['..Q..', '....Q', '.Q...', '...Q.', 'Q....'],
			['...Q.', 'Q....', '..Q..', '....Q', '.Q...'],
			['...Q.', '.Q...', '....Q', '..Q..', 'Q....'],
			['....Q', '.Q...', '...Q.', 'Q....', '..Q..'],
			['....Q', '..Q..', 'Q....', '...Q.', '.Q...'],
		])
	})
})
