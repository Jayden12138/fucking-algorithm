import { getOrder } from '@/1834.单线程-cpu'
import { describe, expect, it } from 'bun:test'

describe('1834.单线程-cpu', () => {
	it('getOrder: [[1,2],[2,4],[3,2],[4,1]]', () => {
		expect(
			getOrder([
				[1, 2],
				[2, 4],
				[3, 2],
				[4, 1],
			])
		).toEqual([0, 2, 3, 1])
	})

	it('getOrder: [[7,10],[7,12],[7,5],[7,4],[7,2]]', () => {
		expect(
			getOrder([
				[7, 10],
				[7, 12],
				[7, 5],
				[7, 4],
				[7, 2],
			])
		).toEqual([4, 3, 2, 0, 1])
	})

	it('2/36 wrong', () => {
		expect(
			getOrder([
				[19, 13],
				[16, 9],
				[21, 10],
				[32, 25],
				[37, 4],
				[49, 24],
				[2, 15],
				[38, 41],
				[37, 34],
				[33, 6],
				[45, 4],
				[18, 18],
				[46, 39],
				[12, 24],
			])
		).toEqual([6, 1, 2, 9, 4, 10, 0, 11, 5, 13, 3, 8, 12, 7])
	})
})
